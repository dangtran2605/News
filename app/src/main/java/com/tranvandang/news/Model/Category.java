package com.tranvandang.news.Model;

public class Category {
    String key, name;

    public Category() {
    }

    public Category(String name) {
        this.name = name;
    }

    public String getKey() {
        return key;
    }

    public void setKey(String key) {
        this.key = key;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
